        "\xf0\x9f\x87\xbf\xf0\x9f\x87\xb2" => 'REGIONAL_INDICATOR_SYMBOL_LETTERS_ZM',
        "\xf0\x9f\x87\xbf\xf0\x9f\x87\xbc" => 'REGIONAL_INDICATOR_SYMBOL_LETTERS_ZW',
    ];

    /**
     * @param list<string> $newKeys
     * @param array<string ,string> $currentDataNames
     * @return array<string, string>
     */
    public function map(array $newKeys, array $currentDataNames): array
    {
        $propertyNames = [];
        foreach ($newKeys as $dataName) {
            $propertyNames[$dataName] = $this->sanitise($dataName);
        }

        // sort by key to ensure consistent order
        ksort($propertyNames);
        // sort by value to ensure the input where the sanitised name is the same is consistent
        asort($propertyNames);

        foreach ($propertyNames as $dataName => $propertyName) {
            $tmpPropertyName = $propertyName;
            $count = 1;
            while (isset($currentDataNames[$tmpPropertyName])) {
                $count++;
                $tmpPropertyName = rtrim($propertyName, '_') . '_' . $count;
            }

            $currentDataNames[$tmpPropertyName] = $dataName;
        }

        return $currentDataNames;
    }

    public function sanitise(string $dataName): string
    {
        $mapping = [...self::CHARACTER_MAPPING, ...self::EMOJI_MAPPING];
        uksort($mapping, static fn($a, $b): int => strlen($b) <=> strlen($a));
        $dataName = str_replace(array_keys($mapping), array_values($mapping), $dataName);

        $dataName = mb_ereg_replace('[^a-zA-Z0-9_]', '_', $dataName) ?? throw new RuntimeException('Encoding error');

        if (preg_match('/^[^a-zA-Z_]/', $dataName)) {
            return '_' . $dataName;
        }

        return $dataName;
    }
